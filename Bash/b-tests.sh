# -d FILE True if file is a directory
# -e FILE True if file exists
# -f FILE True if file exists and is a regular file, example it's not a symlink
# -r FILE True if file exists and is readable
# -s FILE True if file exists and is not empty
# -w FILE True if file exists and is writable
# -x FILE True if file exists and is executable
# -z STRING True if the length of STRING is zero, string is empty
# -n STRING True if the length of STRING is non-zero, string is not empty
# -eq NUM1 NUM2 True if NUM1 is equal to NUM2
# -ne NUM1 NUM2 True if NUM1 is not equal to NUM2
# -l FILE True if file exists and is a symbolic link
